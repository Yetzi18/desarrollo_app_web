// Crea una función que calcule el promedio de los tres primeros elementos de un arreglo 
// numérico y muestre el resultado con un alert.

function promedioTresPrimeros() {
    let numeros = [4,3,7,5,1]
    let promedio = (numeros[0]+numeros[1]+numeros[2])/3;
    alert("El promedio de los tres primeros es: " + promedio);
}
